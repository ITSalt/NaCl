import { randomBytes } from "node:crypto";
import { LifecycleError } from "./contracts.mjs";
import { createProcessRunner } from "./process-runner.mjs";

const KEYCHAIN_REFERENCE = /^keychain:([^/\s]{1,128})\/([^/\s]{1,128})$/;
const ENV_REFERENCE = /^env:([A-Z][A-Z0-9_]{2,127})$/;

export function parseSecretReference(reference) {
  const keychain = KEYCHAIN_REFERENCE.exec(reference ?? "");
  if (keychain) return { kind: "keychain", service: keychain[1], account: keychain[2] };
  const environment = ENV_REFERENCE.exec(reference ?? "");
  if (environment) return { kind: "environment", name: environment[1] };
  throw new LifecycleError(
    "SECRET_REFERENCE_INVALID",
    "The instance contains an unsupported opaque secret reference.",
  );
}

function assertSecretValue(value) {
  if (typeof value !== "string" || value.length < 24 || /[\r\n\0]/.test(value)) {
    throw new LifecycleError(
      "SECRET_VALUE_INVALID",
      "The secret provider returned an invalid value.",
    );
  }
  return value;
}

export function generateGraphSecret() {
  return randomBytes(32).toString("base64url");
}

export class MacOsKeychainSecretProvider {
  constructor(options = {}) {
    this.platform = options.platform ?? process.platform;
    this.runner = options.runner ?? createProcessRunner();
  }

  #fields(reference) {
    const parsed = parseSecretReference(reference);
    if (parsed.kind !== "keychain") {
      throw new LifecycleError("SECRET_REFERENCE_INVALID", "A Keychain reference is required.");
    }
    if (this.platform !== "darwin") {
      throw new LifecycleError(
        "SECRET_BACKEND_UNAVAILABLE",
        "The configured secret backend requires macOS Keychain.",
        { status: "BLOCKED" },
      );
    }
    return parsed;
  }

  async exists(reference) {
    const { service, account } = this.#fields(reference);
    const result = await this.runner.run({
      command: "/usr/bin/security",
      args: ["find-generic-password", "-a", account, "-s", service],
      sensitiveOutput: true,
    });
    if (result.status === 0) return true;
    if (result.status === 44) return false;
    throw new LifecycleError(
      "SECRET_BACKEND_UNAVAILABLE",
      "Keychain availability could not be determined.",
      { status: "BLOCKED" },
    );
  }

  async create(reference, value) {
    const { service, account } = this.#fields(reference);
    assertSecretValue(value);
    const result = await this.runner.run({
      command: "/usr/bin/security",
      args: ["add-generic-password", "-a", account, "-s", service, "-w"],
      input: `${value}\n`,
      sensitiveValues: [value],
      sensitiveOutput: true,
    });
    if (result.status !== 0) {
      throw new LifecycleError(
        "SECRET_CREATE_FAILED",
        "Keychain did not create a new project secret; existing values are never overwritten.",
        { status: "BLOCKED" },
      );
    }
  }

  async get(reference) {
    const { service, account } = this.#fields(reference);
    const result = await this.runner.run({
      command: "/usr/bin/security",
      args: ["find-generic-password", "-a", account, "-s", service, "-w"],
      sensitiveOutput: true,
    });
    if (result.status !== 0) {
      throw new LifecycleError(
        result.status === 44 ? "SECRET_MISSING" : "SECRET_REVOKED",
        "The project secret is missing, locked, or revoked.",
        { status: "BLOCKED" },
      );
    }
    return assertSecretValue(result.sensitiveStdout?.trimEnd());
  }
}

export class EnvironmentSecretProvider {
  constructor(environment = process.env) {
    this.environment = environment;
  }

  async exists(reference) {
    const parsed = parseSecretReference(reference);
    if (parsed.kind !== "environment") {
      throw new LifecycleError("SECRET_REFERENCE_INVALID", "An environment reference is required.");
    }
    return this.environment[parsed.name] !== undefined;
  }

  async create() {
    throw new LifecycleError(
      "SECRET_BACKEND_READ_ONLY",
      "Environment secret references cannot be generated by local init.",
      { status: "BLOCKED" },
    );
  }

  async get(reference) {
    const parsed = parseSecretReference(reference);
    if (parsed.kind !== "environment") {
      throw new LifecycleError("SECRET_REFERENCE_INVALID", "An environment reference is required.");
    }
    const value = this.environment[parsed.name];
    if (value === undefined) {
      throw new LifecycleError("SECRET_MISSING", "The referenced environment secret is missing.", {
        status: "BLOCKED",
      });
    }
    return assertSecretValue(value);
  }
}

export class MemorySecretProvider {
  constructor(entries = {}) {
    this.entries = new Map(Object.entries(entries));
    this.revoked = new Set();
  }

  async exists(reference) {
    parseSecretReference(reference);
    return this.entries.has(reference);
  }

  async create(reference, value) {
    parseSecretReference(reference);
    if (this.entries.has(reference)) {
      throw new LifecycleError(
        "SECRET_CREATE_FAILED",
        "The project secret already exists and was not overwritten.",
        { status: "BLOCKED" },
      );
    }
    this.entries.set(reference, assertSecretValue(value));
    this.revoked.delete(reference);
  }

  async get(reference) {
    parseSecretReference(reference);
    if (this.revoked.has(reference)) {
      throw new LifecycleError("SECRET_REVOKED", "The project secret was revoked.", {
        status: "BLOCKED",
      });
    }
    if (!this.entries.has(reference)) {
      throw new LifecycleError("SECRET_MISSING", "The project secret is missing.", {
        status: "BLOCKED",
      });
    }
    return assertSecretValue(this.entries.get(reference));
  }

  revoke(reference) {
    this.revoked.add(reference);
  }
}
